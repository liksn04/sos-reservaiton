## 제1조 (목적)

본 약관은 운영자가 제공하는 서비스의 이용 조건과 절차, 회원과 운영자의 권리·의무 및 책임 사항, 기타 필요한 사항을 규정하는 것을 목적으로 합니다.

## 제2조 (용어의 정의)

1. **"서비스"** 란 운영자가 PC, 모바일(PWA), 기타 기기를 통하여 제공하는 합주실 예약, 행사 관리, 회비/예산 관리, 회원 관리 등을 위한 모든 기능을 의미합니다.
2. **"회원"** 이란 본 약관에 동의하고 카카오 OAuth 등 운영자가 허용한 방법으로 로그인하여 서비스를 이용하는 자를 말합니다.
3. **"관리자"** 란 운영자가 지정한 권한 보유 회원으로, 회원 승인·차단, 예약 정책 운영, 행사·예산 관리 등 운영 기능을 수행하는 자를 말합니다.
4. **"예약"** 이란 회원이 서비스를 통하여 합주실 사용 일시·인원·목적을 지정하여 등록하는 행위를 말합니다.
5. **"콘텐츠"** 란 회원이 서비스 이용 과정에서 입력·등록하는 텍스트, 이미지, 일정, 거래 내역 등 일체의 정보를 말합니다.

## 제3조 (약관의 게시와 개정)

1. 운영자는 본 약관의 내용을 서비스 화면 또는 연결 화면을 통해 회원이 쉽게 확인할 수 있도록 게시합니다.
2. 운영자는 관계 법령을 위배하지 않는 범위에서 본 약관을 개정할 수 있으며, 개정 시 적용일자와 사유를 명시하여 시행일로부터 최소 7일 전(회원에게 불리하거나 중대한 변경의 경우 30일 전)에 서비스 내 공지 또는 동아리 공식 채널을 통해 공지합니다.
3. 회원이 개정 약관 시행일 이후에도 서비스를 계속 이용하는 경우 변경된 약관에 동의한 것으로 봅니다. 동의하지 않는 회원은 회원 탈퇴를 통해 이용을 중단할 수 있습니다.

## 제4조 (이용계약의 체결)

1. 이용계약은 이용을 희망하는 자가 본 약관에 동의한 후 카카오 OAuth 등 운영자가 정한 인증 절차를 완료하고, 운영자(또는 관리자)가 이를 승인함으로써 체결됩니다.
2. 운영자는 다음 각 호에 해당하는 신청에 대해 승인을 보류하거나 거절, 사후 해지할 수 있습니다.
   - 타인의 명의나 정보를 이용한 경우
   - 동아리 회원이 아님이 확인되는 경우
   - 허위의 정보를 기재한 경우
   - 과거 본 약관 또는 관계 법령 위반으로 차단·탈퇴된 이력이 있는 경우
   - 기타 운영자가 운영 정책상 부적절하다고 합리적으로 판단하는 경우
3. 운영자는 회원 승인 전 단계에서 예약·행사 등 일부 기능 접근을 제한할 수 있으며, 차단된 회원에 대해서는 별도의 안내 화면을 통해 이용을 제한합니다.

## 제5조 (회원 정보의 관리)

1. 회원은 표시 이름, 세션(파트) 등 프로필 정보가 변경된 경우 지체 없이 서비스 내 프로필 화면에서 직접 수정하여야 합니다.
2. 회원은 자신의 계정 및 인증 정보(카카오 계정 등)를 제3자에게 양도·대여·공유할 수 없으며, 이로 인해 발생한 손해는 회원이 부담합니다.
3. 운영자는 회원이 입력한 정보가 사실과 다르거나 부적절하다고 판단되는 경우 수정·삭제 등 필요한 조치를 요청할 수 있습니다.

## 제6조 (서비스의 제공 및 변경)

1. 운영자는 회원에게 다음과 같은 서비스를 제공합니다.
   - 합주실 예약 등록·수정·삭제 및 초대 인원 관리
   - 월간 캘린더 및 일정 조회
   - 행사 등록, 참가 신청, 출석 체크
   - 회비/예산 거래 내역 관리(관리자 기능 포함)
   - 회원 조회·관리, 권한 및 차단 관리(관리자 기능)
   - 예약 리마인드 등 푸시 알림(웹 푸시 구독 시)
   - 기타 운영자가 추가로 정하는 서비스
2. 운영자는 서비스의 품질 향상, 보안 및 운영상의 필요에 따라 제공 기능을 변경·추가·축소할 수 있으며, 중요한 변경 사항은 사전에 공지합니다.
3. 무료로 제공되는 서비스의 특성상, 운영자는 일부 기능을 사전 통지 없이 변경하거나 중단할 수 있습니다.

## 제7조 (서비스의 중단)

1. 운영자는 다음 각 호의 사유가 발생한 경우 서비스 제공을 일시적으로 중단할 수 있습니다.
   - 시스템 점검, 보수, 교체 또는 통신 두절
   - 천재지변, 정전, 서비스 설비 장애
   - Supabase, Vercel 등 운영자가 이용하는 외부 서비스의 장애
   - 기타 운영상 부득이한 사유
2. 운영자는 사전 공지가 가능한 경우 공지 후 중단하며, 부득이한 경우 사후에 공지할 수 있습니다.

## 제8조 (회원의 의무)

회원은 다음 각 호의 행위를 하여서는 안 됩니다.

1. 신청 또는 변경 시 허위 내용을 등록하는 행위
2. 타인의 정보를 도용하거나 무단 사용하는 행위
3. 운영자가 게시한 정보를 무단으로 변경, 삭제, 외부 공개하는 행위
4. 운영자와 제3자의 저작권 등 지적재산권을 침해하는 행위
5. 운영자 및 다른 회원의 명예를 손상시키거나 업무를 방해하는 행위
6. 외설적이거나 폭력적인 내용, 기타 공공질서 및 미풍양속에 반하는 콘텐츠를 게시하는 행위
7. 동아리 운영과 무관한 영리 목적의 행위(광고, 호객, 상행위 등)
8. 합주실 예약을 비정상적인 방법으로 선점·중복 등록하거나 타인의 예약을 방해하는 행위
9. 관계 법령 및 본 약관, 운영자가 별도로 고지하는 운영 정책을 위반하는 행위

## 제9조 (예약 정책 및 합주실 이용)

1. 회원은 운영자가 정한 예약 정책(예: 시간 겹침 불가, 과거 시점 예약 제한, 당일 합주 정책, 시즌별 정책 등)을 준수하여야 합니다.
2. 예약을 한 회원은 예약 시간을 성실히 준수하고, 부득이하게 이용이 어려운 경우 즉시 예약을 수정 또는 취소하여 다른 회원이 이용할 수 있도록 협조하여야 합니다.
3. 정당한 사유 없이 반복적으로 예약을 이행하지 않거나, 예약 정책을 우회·악용하는 회원에 대하여 운영자(또는 관리자)는 다음 각 호의 조치를 단계적으로 적용할 수 있습니다.
   - 경고
   - 일정 기간 예약 기능 제한
   - 차단(BannedPage 안내) 및 계정 이용 정지
   - 회원 자격 박탈

## 제10조 (관리자 권한)

1. 관리자는 운영의 필요성에 따라 회원 정보의 조회, 예약·행사·거래 내역의 수정·삭제, 회원 승인·차단, 정책 시즌 운영, 감사 로그 열람 등의 권한을 행사할 수 있습니다.
2. 관리자는 권한 행사 시 본 약관, 개인정보 처리방침 및 운영자의 운영 정책에 따라 합리적인 범위에서 권한을 행사하여야 하며, 권한을 남용하여서는 안 됩니다.
3. 관리자 권한의 부여 및 해제는 운영자(또는 기존 관리자)에 의해 이루어집니다.

## 제11조 (게시물 및 데이터의 관리)

1. 회원이 서비스 내에 입력·등록한 콘텐츠(예약 정보, 행사 정보, 거래 내역, 프로필 등)의 저작권 및 책임은 원칙적으로 해당 회원에게 있습니다.
2. 회원은 운영자가 서비스 제공, 백업, 운영 통계, 약관 위반 검토 등 운영상 합리적인 목적으로 회원의 콘텐츠를 이용하는 것에 동의합니다. 단, 이는 본 약관과 개인정보 처리방침이 허용하는 범위 내에서만 이루어집니다.
3. 운영자는 다음 각 호에 해당하는 콘텐츠를 회원에게 사전 통지 없이 삭제·이동하거나 등록 거부할 수 있습니다.
   - 본 약관 또는 관계 법령에 위반되는 콘텐츠
   - 타인의 명예, 사생활, 권리를 침해하는 콘텐츠
   - 서비스의 정상적인 운영을 방해하는 콘텐츠
   - 음란성, 폭력성, 차별, 혐오 표현 등을 포함한 콘텐츠

## 제12조 (지적재산권)

1. 서비스 자체의 디자인, 소스 코드, 로고, 텍스트, 이미지 등 운영자가 작성·제작한 저작물의 저작권은 운영자에게 귀속됩니다.
2. 회원은 운영자의 사전 서면 동의 없이 서비스의 일부 또는 전부를 복제·전송·출판·배포·방송하거나 제3자에게 이용하게 할 수 없습니다.

## 제13조 (계약 해지 및 이용 제한)

1. 회원은 언제든지 서비스 내 "회원 탈퇴" 기능을 통해 이용계약을 해지할 수 있습니다. 운영자는 회원 탈퇴 즉시 또는 운영자가 정한 처리 절차에 따라 회원 정보를 처리합니다(세부 사항은 개인정보 처리방침 참조).
2. 운영자는 회원이 본 약관 또는 관계 법령을 위반한 경우 사전 통지 후(긴급한 경우 사후 통지) 다음 각 호의 조치를 취할 수 있습니다.
   - 일정 기간 기능 제한
   - 차단 또는 이용 정지
   - 이용계약의 해지
3. 회원 탈퇴 또는 강제 해지 후에도 관계 법령에 따라 보관이 요구되는 정보는 해당 법령이 정한 기간 동안 분리 보관됩니다.

## 제14조 (책임의 제한)

1. 운영자는 비영리 동아리 운영 보조 목적으로 서비스를 제공하며, 천재지변, 외부 서비스(예: Supabase, Vercel, 카카오) 장애, 회원의 귀책 사유로 인하여 서비스를 제공할 수 없는 경우 책임을 부담하지 않습니다.
2. 운영자는 회원이 서비스에 등록한 콘텐츠의 정확성·신뢰성·적법성을 보증하지 않으며, 이로 인한 분쟁에 대해서는 당사자 간 해결을 원칙으로 합니다.
3. 운영자는 회원 상호 간 또는 회원과 제3자 간에 서비스를 매개로 발생한 분쟁에 개입할 의무를 부담하지 않으며, 이로 인한 손해를 배상할 책임도 없습니다.
4. 본 서비스는 무료로 제공되므로, 운영자의 고의 또는 중대한 과실이 없는 한 회원에게 발생한 손해에 대해 운영자는 책임을 부담하지 않습니다.

## 제15조 (개인정보의 보호)

1. 운영자는 관련 법령이 정하는 바에 따라 회원의 개인정보를 보호하기 위해 노력합니다.
2. 개인정보의 수집 항목, 이용 목적, 보유 기간 등 세부 사항은 별도의 [개인정보 처리방침](/legal/privacy)에서 정합니다.

## 제16조 (분쟁 해결 및 준거법)

1. 본 약관의 해석 및 운영자와 회원 간 분쟁에 대하여는 대한민국 법령을 준거법으로 합니다.
2. 서비스 이용으로 발생한 분쟁에 대하여 소송이 제기되는 경우 민사소송법 등 관련 법령이 정한 절차에 따른 관할 법원을 전속관할 법원으로 합니다.

