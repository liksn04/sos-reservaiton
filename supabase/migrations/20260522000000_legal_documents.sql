-- ============================================================
-- Legal documents (Terms of Service / Privacy Policy)
-- Admin-editable Markdown documents with public read access.
-- ============================================================

create table if not exists public.legal_documents (
  slug text primary key,
  title text not null,
  intro text not null default '',
  body text not null default '',
  effective_date date not null default current_date,
  updated_at timestamptz not null default now(),
  updated_by uuid references public.profiles(id) on delete set null
);

comment on table public.legal_documents is 'Admin-editable legal documents (terms, privacy, etc.).';
comment on column public.legal_documents.slug is 'Stable identifier used in URLs, e.g. "terms" or "privacy".';
comment on column public.legal_documents.body is 'Markdown body. Rendered with react-markdown + remark-gfm on the client.';

create or replace function public.touch_legal_documents_updated_at()
returns trigger
language plpgsql
security invoker
set search_path = public, pg_catalog
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists legal_documents_touch_updated_at on public.legal_documents;
create trigger legal_documents_touch_updated_at
  before update on public.legal_documents
  for each row execute function public.touch_legal_documents_updated_at();

alter table public.legal_documents enable row level security;

drop policy if exists "Legal documents are publicly readable" on public.legal_documents;
create policy "Legal documents are publicly readable"
  on public.legal_documents
  for select
  using (true);

drop policy if exists "Admins can insert legal documents" on public.legal_documents;
create policy "Admins can insert legal documents"
  on public.legal_documents
  for insert
  to authenticated
  with check (public.is_admin_user());

drop policy if exists "Admins can update legal documents" on public.legal_documents;
create policy "Admins can update legal documents"
  on public.legal_documents
  for update
  to authenticated
  using (public.is_admin_user())
  with check (public.is_admin_user());

drop policy if exists "Admins can delete legal documents" on public.legal_documents;
create policy "Admins can delete legal documents"
  on public.legal_documents
  for delete
  to authenticated
  using (public.is_admin_user());

